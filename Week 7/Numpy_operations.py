import numpy as np

a = np.array([[1, 2], [4, 5]])
b = np.array([[1, 2], [4, 5]])

print("Addition:\n", np.add(a, b))
print("Subtraction:\n", np.subtract(a, b))
print("Multiplication:\n", np.multiply(a, b))
print("Division:\n", np.divide(a, b))
print("Dot Product:\n", np.dot(a, b))
print("Exponentiation:\n", np.exp(a))
print("Logarithm:\n", np.log(a))
print("Natural Logarithm (log10):\n", np.log10(a))